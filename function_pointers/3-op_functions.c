/**
 * op_add - sum two integers
 * @a: integer 1
 * @b: integer 2
 * Return:  the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two integers
 * @a: integer 1
 * @b: integer 2
 * Return:  the sub  of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mult two integers
 * @a: integer 1
 * @b: integer 2
 * Return:  the mult of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: integer 1
 * @b: integer 2
 * Return:  the divide  of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod  two integers
 * @a: integer 1
 * @b: integer 2
 * Return:  the mod  of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
